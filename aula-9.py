adicao = 10 + 10
print('Adição:', adicao)

subtracao = 10 - 5
print('Subtração:', subtracao)

multiplicacao = 10 * 10
print('Multiplicação:', multiplicacao)

# Realiza a divisão de dois números, resultando em ponto flutuante
divisao = 10 / 3
print('Divisão:', divisao)

# Realiza a divisão inteira, resultando em número inteiro
divisao_inteira = 10 // 3
print('Divisão inteira:', divisao_inteira)

# Realiza a exponenciação de 2 elevado a 10
exponenciacao = 2 ** 10
print('Exponenciação:', exponenciacao)

# Calcula o módulo, que retorna o resto da divisão
modulo = 55 % 2
print('Módulo:', modulo)

# Verifica se os números são divisíveis por 8 ou 2
