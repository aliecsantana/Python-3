# Soma de dois números inteiros
n1 = int(input('Digite um número: '))  # Entrada do primeiro número
n2 = int(input('Digite mais um número: '))  # Entrada do segundo número
s = n1 + n2  # Calcula a soma
print('A soma vale:', s)  # Exibe o resultado da soma

# Exemplos de tipos de dados em Python:
# int: 7, -4, 0, 9875
# float: 4.5, 0.075, -15.223, 7.0
# bool: True, False
# str: 'Olá', '7.5', ''

# Outras formas de exibir o resultado
print('A soma vale', s)  # Exibe o resultado sem pontuação
print('A soma vale: {}'.format(s))  # Exibe o resultado com formatação
