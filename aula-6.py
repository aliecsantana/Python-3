# Conversão de tipos (type conversion) é o processo de transformar um valor de um tipo para outro, como de str para int, float ou bool
print(int('1'), type(int('1'))) # Convertendo de str para int
print(type(float('1') + 1)) # Convertendo de str para float e somando
print(str(11) + 'b') # Convertendo de int para str e concatenando