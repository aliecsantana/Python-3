# Exemplo de saída no terminal
print('Olá, mundo!')  # Exibe "Olá, mundo!"

# Operações com números e strings
print(7 + 4)  # Soma: 11
print('7' + '4')  # Concatenação: "74"

# Combinação de tipos no print
print('Olá,', 5, '!')  # Exibe: "Olá, 5!"

# Declaração e exibição de variáveis
nome = 'Guanabara'
idade = 25
peso = 75.8
print(nome, idade, peso)  # Exibe: "Guanabara 25 75.8"

# Entrada de dados do usuário
nome = input('Qual é o seu nome? ')  # Captura o nome
idade = input('Quantos anos você tem? ')  # Captura a idade
peso = input('Quanto você pesa? ')  # Captura o peso
print(nome, idade, peso)  # Exibe as respostas do usuário
